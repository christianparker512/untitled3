'use strict';

//push adds it to the end of the array
const colors = ['red', 'blue', 'yellow'];

colors.push('orange');
console.log(colors);

//removes the last thing in the array
const parkers = ['chris', 'abby', 'lisa'];
parkers.pop();
console.log(parkers);

//shift removes the first
const bobs = ['matt', 'rob', 'brad', 'trace','chris'];
bobs.shift();
console.log(bobs)
